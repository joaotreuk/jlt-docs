// Objeto com informações sobre o navegador

// Propriedades
navigator.appCodeName // Retorna o nome código do navegador, todos navegadores modernos retornam 'Mozilla'
navigator.appName // Retorna o nome do navegador, navegadores modernos retornam 'Netscape'
navigator.appVersion // Informações sobre as versões dos navegadores modernos
navigator.cookieEnabled // Os Cookies estão habilitados ou não
navigator.geolocation // Retorna um objeto de geolocalização
navigator.language // Apenas leitura, obtem a linguagem atual do navegador
navigator.languages // Retorna uma matriz de linguagens de preferência do usuário, na ordem da linguagem de mais preferência para a de menos
navigator.onLine // Se o navegador está online ou não
navigator.platform // Retorna o sistema operacional no qual o navegador está instalado, ex: Win32
navigator.product // Todos navegadores retornam 'Gecko'
navigator.userAgent // Apenas leitura, informações sobre as versões dos navegadores modernos

// Métodos
navigator.javaEnabled() // Retorna se Java está habilitado para o navegador ou não